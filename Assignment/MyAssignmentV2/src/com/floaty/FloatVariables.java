package com.floaty;

public class FloatVariables {

	
	
			float a = 4f;
			float b = 5f;
			public FloatVariables(float a, float b) {
				super();
				this.a = a;
				this.b = b;
			}
			
			
			
			
			
			@Override
			public String toString() {
				return "AccessFloat [a=" + a + ", b=" + b + "]";
			}
			
			
	

}
