package com.floating;

public class FloatVariable {

	
	
			float one = 4f;
			float two = 5f;
			public FloatVariable(float one, float two) {
				super();
				this.one = one;
				this.two = two;
			}		
			
			@Override
			public String toString() {
				return "Float [one=" +one  + ", two=" + two + "]";
			}
			
			
	

}
